// Switch Case 

var nilai = "A"

switch (nilai) {
    
    case 95:
        console.log ("A")
        break
        
    case 90:
        console.log ("B")
        break
    
    case 85:
        console.log ("C")
        break
        
    case 80:
        console.log ("D")
        break
        
    case 75:
        console.log ("E")
        
    default:
    console.log ("Semula")
}




